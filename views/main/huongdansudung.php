						<p><span>Do viên ngọc trai được bọc một lớp xà cừ là chất hữu cơ nên khi sử dụng bạn nên tránh không để tiếp xúc với các loại nước hoa, sữa dưỡng da, nước lau sơn móng tay, các loại keo xịt tóc, chanh, giấm, … và các loại chất tẩy hoặc hóa chất dễ bay hơi khác. Vì nếu tiếp xúc với các chất này viên ngọc trai sẽ bị bào mòn dần. Tốt nhất trong thời gian sử dụng, bạn nên ngâm rửa bằng nước ấm (không nên dùng bàn chải) sau đó lau nhẹ bằng vải mềm cotton thì viên ngọc trai sẽ bóng đẹp tự nhiên.</span></p>
