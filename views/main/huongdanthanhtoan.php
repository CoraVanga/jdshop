 				<div class="question"><span class="mp-sprite icon"></span>

 					<h2 class="strong-note">1-Thanh toán tiền mặt khi nhận hàng (COD)</h2>
 				</div>

 				<div class="answer">Là cách thanh toán dễ dàng nhất khi quý khách không quen với các hình thức thanh toán trực tuyến.<br />
 					- Ngay sau khi nhận được đơn hàng, JD sẽ xác nhận với Quý khách qua điện thoại sau đó tiến hành thực hiện đơn hàng và giao hàng trong thời gian quy định. Quý khách được kiểm tra và thanh toán tiền mặt khi nhận hàng.<br />
 					- Quý khách chọn nhận hàng tại cửa hàng sẽ thanh toán tại cửa hàng bằng các hình thức tiền mặt, cà thẻ, trả góp.<br />
 					- Quý khách nhận hàng tận nơi sẽ thanh toán tiền mặt cho nhà vận chuyển.<br />
 					<strong>Lưu ý: </strong> Thanh toán tiền mặt khi nhận hàng (COD) chỉ áp dụng cho các đơn hàng được giao ở khu vục Hồ Chí Minh và Hà Nội.</div>
 				</div>

