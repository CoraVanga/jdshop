 	<div class="pages-JD">
 		<p>Tại JD.com.vn, khách hàng có thể mua sắm trực tuyến thuận lợi hơn với nhiều hình thức thanh toán linh hoạt như: thanh toán khi nhận hàng, thanh toán bằng thẻ tín dụng, thẻ ghi nợ quốc tế, thẻ ATM, trả góp lãi suất 0%, thanh toán qua tài khoản ngân hàng, chuyển khoản, sử dụng dịch vụ cổng thanh toán trực tuyến.</p>

